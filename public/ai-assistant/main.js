async function bootstrap() {
  console.info("[App] Digital Twin Assistant — booting…");

  // 1. Resolve API key
  const apiKey = resolveApiKey();
  if (!apiKey) {
    console.error("[App] No NVIDIA API key found. Append ?key=nvapi-... to the URL.");
    document.body.innerHTML = `
      <div style="display:flex;align-items:center;justify-content:center;height:100vh;font-family:monospace;color:#ef4444;background:#080c14;flex-direction:column;gap:16px;padding:32px;text-align:center;">
        <h2 style="font-size:18px;margin:0;">API Key Not Found</h2>
        <p style="color:#8b95a8;font-size:13px;max-width:480px;line-height:1.7;">
          No NVIDIA API key was detected. Provide it via the URL:<br>
          <code style="color:#60a5fa;">/ai-assistant/index.html?key=nvapi-YOUR_KEY</code><br><br>
          Or set <code style="color:#60a5fa;">VITE_NVIDIA_API_KEY</code> in your <code>.env</code> file.
        </p>
      </div>
    `;
    return;
  }

  // 2. Load CNC Knowledge Base
  const knowledgeBase = await loadKnowledgeBase();
  const systemPrompt = buildSystemPrompt(knowledgeBase);
  console.info("[App] System prompt built (%d chars)", systemPrompt.length);

  // 3. Instantiate core modules
  const ui = new UIController();
  const conversation = new ConversationManager(systemPrompt);
  const nimClient = new NvidiaStreamClient(apiKey);
  const tts = new VoiceSynthController();

  const actionInterceptor = new ActionInterceptor((actionType, rawToken) => {
    // Fire visual badge + log
    ui.triggerActionBadge(actionType, rawToken);
    ui.logInterceptedAction(actionType, rawToken);
    console.info(`[ActionInterceptor] Mechanical event → ${actionType}`);
  });

  const voiceInput = new VoiceInputController({
    onStart: () => {
      ui.setMicActive(true);
    },
    onResult: (transcript) => {
      const ta = ui.els.userInput;
      ta.value += (ta.value ? " " : "") + transcript;
      ui._updateCharCount(ta.value.length);
    },
    onEnd: () => {
      ui.setMicActive(false);
    },
    onError: (err) => {
      ui.setMicActive(false);
      console.error("[VoiceInput]", err);
    },
  });



  // 4. Wire up event listeners

  // Char count
  ui.els.userInput.addEventListener("input", () => {
    ui._updateCharCount(ui.els.userInput.value.length);
  });

  // Ctrl+Enter to submit
  ui.els.userInput.addEventListener("keydown", (e) => {
    if ((e.ctrlKey || e.metaKey) && e.key === "Enter") {
      e.preventDefault();
      sendQuery();
    }
  });

  // Send button
  ui.els.sendBtn.addEventListener("click", sendQuery);

  // Stop streaming
  ui.els.stopBtn.addEventListener("click", () => {
    nimClient.abort();
  });

  // Mic toggle
  ui.els.micBtn.addEventListener("click", () => {
    if (!voiceInput.supported) {
      alert("Speech recognition is not supported in your current browser. Try Chrome or Edge.");
      return;
    }
    voiceInput.toggle();
  });

  // TTS toggle
  ui.els.ttsToggleBtn.addEventListener("click", () => {
    const isOn = tts.toggle();
    ui.setTtsActive(isOn);
  });

  // Clear conversation
  ui.els.clearBtn.addEventListener("click", () => {
    if (!confirm("Clear the entire conversation history?")) return;
    conversation.clear();
    ui._renderWelcome();
    console.info("[App] Conversation history cleared");
  });

  // 5. Core send query function
  let currentAbort = null;

  // Global variables to manage the asynchronous audio streaming queue
  let audioQueue = [];
  let isAudioPlaying = false;
  // Resolved once per user turn from the user's own message text
  let activeTtsConfig = tts._getTtsConfig(""); // default English

  /**
   * Sequential processing worker for the audio queue.
   * Uses the turn-level activeTtsConfig — never re-detects per sentence.
   */
  async function processAudioQueue() {
    if (isAudioPlaying || audioQueue.length === 0) {
      return;
    }

    isAudioPlaying = true;
    const nextSentence = audioQueue.shift();

    tts.speak(nextSentence, activeTtsConfig, () => {
      isAudioPlaying = false;
      processAudioQueue();
    });
  }

  async function sendQuery() {
    const rawInput = ui.els.userInput.value.trim();
    if (!rawInput) return;

    ui.setStatus("loading", "Connecting...");

    // Append user message to UI and conversation history
    ui.appendUserMessage(rawInput);
    conversation.pushUser(rawInput);
    ui.clearInput();

    // Detect language ONCE from the user's message — locked for the entire AI turn
    activeTtsConfig = tts._getTtsConfig(rawInput);

    // Set UI into streaming mode
    ui.setInputLocked(true);
    ui.openStreamingScreen();

    let fullResponse = "";      // accumulates visible content
    let firstTokenSeen = false;
    let sentenceBuffer = "";

    // Reset audio queue state and interrupt any active playback for the new turn
    audioQueue = [];
    isAudioPlaying = false;
    tts.stop();

    currentAbort = nimClient.stream(conversation.buildPayload(), {
      onToken(token) {
        if (!firstTokenSeen) {
          firstTokenSeen = true;
          ui.setStatus("loading", "Streaming…");
        }

        // Strip action tokens from the visible stream BEFORE display
        const cleaned = actionInterceptor.process(token);
        if (cleaned) {
          fullResponse += cleaned;
          ui.appendStreamToken(cleaned);
          sentenceBuffer += cleaned;

          // Regular expression detecting end of sentence (Period, Question, Exclamation, or Newline)
          if (/[\.\?\!\n]/.test(cleaned)) {
            const completedSentence = sentenceBuffer.trim();
            // Ensure it contains actual text before pushing to speech synthesis
            if (completedSentence.length > 5) {
              audioQueue.push(completedSentence);
              processAudioQueue();
            }
            sentenceBuffer = ""; // Reset internal chunk buffer
          }
        }
      },

      onReasoningToken(token) {
        // Diagnostic only
        // console.debug("[Reasoning]", token);
      },

      onDone() {
        // Close streaming screen and render final AI message
        ui.closeStreamingScreen();

        // Flush any residual text remaining inside the buffer
        const remainingText = sentenceBuffer.trim();
        if (remainingText.length > 0) {
          audioQueue.push(remainingText);
          processAudioQueue();
        }

        const finalResponse = fullResponse.trim();
        if (finalResponse) {
          ui.appendAiMessage(finalResponse);
          conversation.pushAssistant(finalResponse);
        }

        // Keep UI locked until the voice assistant completely finishes speaking the entire queue
        const checkAudioFinished = setInterval(() => {
          if (audioQueue.length === 0 && !isAudioPlaying) {
            clearInterval(checkAudioFinished);
            ui.setStatus("idle", "Ready");
            ui.setInputLocked(false);
            ui.els.userInput.focus();
          }
        }, 200);
      },

      onError(err) {
        console.error("[App] Stream error:", err);
        ui.closeStreamingScreen();
        ui.appendErrorMessage(`Connection error: ${err.message}`);
        ui.setStatus("error", "Error");
        ui.setInputLocked(false);
      },
    });
  }

  console.info("[App] Digital Twin Assistant — ready ✓");
}

// Entry point
bootstrap().catch((err) => {
  console.error("[App] Fatal bootstrap error:", err);
});
