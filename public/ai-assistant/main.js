//  VOICE INPUT — Web Speech API (SpeechRecognition)
// ============================================================

class VoiceInputController {
  constructor({ onResult, onStart, onEnd, onError }) {
    const SpeechRecognition =
      window.SpeechRecognition || window.webkitSpeechRecognition;

    if (!SpeechRecognition) {
      this.supported = false;
      console.warn("[VoiceInput] SpeechRecognition API not supported in this browser.");
      return;
    }

    this.supported = true;
    this.isListening = false;

    this._recognition = new SpeechRecognition();
    this._recognition.lang = "en-US";   // can be toggled to 'it-IT' for Italian input
    this._recognition.interimResults = false;     // only final results fed to input
    this._recognition.maxAlternatives = 1;
    this._recognition.continuous = false;      // single utterance per press

    this._recognition.onstart = () => {
      this.isListening = true;
      onStart?.();
    };

    this._recognition.onresult = (event) => {
      const transcript = event.results[0]?.[0]?.transcript ?? "";
      if (transcript.trim()) onResult(transcript.trim());
    };

    this._recognition.onend = () => {
      this.isListening = false;
      onEnd?.();
    };

    this._recognition.onerror = (event) => {
      this.isListening = false;
      console.error("[VoiceInput] Error:", event.error);
      onError?.(event.error);
    };
  }

  start() {
    if (!this.supported || this.isListening) return;
    this._recognition.start();
  }

  stop() {
    if (!this.supported || !this.isListening) return;
    this._recognition.stop();
  }

  toggle() {
    this.isListening ? this.stop() : this.start();
  }
}


