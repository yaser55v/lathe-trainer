// ============================================================
//  API KEY — read from Vite env variable injected at build time
//  In the public/ folder we cannot use import.meta.env, so we
//  embed via a small inline script tag generated by the parent
//  Vite build, OR we read from a window global set by the loader.
//  For the standalone playground, we fall back to reading the key
//  from the URL query string (?key=...) or a meta tag.
// ============================================================

function resolveApiKey() {
  // 1. Try Vite-injected global (set by vite-define or a small loader)
  if (typeof __NVIDIA_API_KEY__ !== "undefined" && __NVIDIA_API_KEY__) {
    return __NVIDIA_API_KEY__;
  }
  // 2. Try URL query param: ?key=nvapi-...   (convenient for dev sharing)
  const urlKey = new URLSearchParams(window.location.search).get("key");
  if (urlKey) return urlKey;

  // 3. Try <meta name="nvidia-api-key" content="..."> in the HTML head
  const metaEl = document.querySelector('meta[name="nvidia-api-key"]');
  if (metaEl?.content) return metaEl.content;

  return null;
}

