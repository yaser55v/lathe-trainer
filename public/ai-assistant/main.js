// ============================================================

class ConversationManager {
  /** @param {string} systemPrompt */
  constructor(systemPrompt) {
    this._systemMsg = { role: "system", content: systemPrompt };
    this._history = []; // array of { role, content }
  }

  /** @returns {Array<{role:string, content:string}>} */
  buildPayload() {
    return [this._systemMsg, ...this._history];
  }

  /** @param {string} text */
  pushUser(text) {
    this._history.push({ role: "user", content: text.trim() });
  }

  /** @param {string} text — final assembled AI response (without action tokens) */
  pushAssistant(text) {
    if (text.trim()) {
      this._history.push({ role: "assistant", content: text.trim() });
    }
  }

  clear() {
    this._history = [];
  }

  get turnCount() { return Math.floor(this._history.length / 2); }
}

