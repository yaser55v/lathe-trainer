// VOICE SYNTHESIS - Puter SDK + Web Speech Fallback
// All comments and names in English only
// ============================================================

class VoiceSynthController {
  constructor() {
    this.enabled = true;
    this.isSpeaking = false;
    this._currentAudio = null;
    this._sdkLoadingPromise = null;

    // Pre-load SDK to reduce latency
    this._ensureSdkLoaded().catch(() => { });
  }

  async _ensureSdkLoaded() {
    if (typeof puter !== "undefined") {
      puter.quiet = true;
      return;
    }
    if (this._sdkLoadingPromise) return this._sdkLoadingPromise;

    this._sdkLoadingPromise = new Promise((resolve, reject) => {
      const script = document.createElement("script");
      script.src = "https://js.puter.com/v2/";
      script.async = true;
      script.onload = () => {
        if (typeof puter !== "undefined") puter.quiet = true;
        resolve();
      };
      script.onerror = () => {
        this._sdkLoadingPromise = null;
        reject(new Error("Failed to load Puter SDK"));
      };
      document.head.appendChild(script);
    });
    return this._sdkLoadingPromise;
  }

  /**
   * Detects language from text and returns TTS config.
   * Called ONCE per user turn (on the user's message), not per sentence.
   * @param {string} text - User input text
   * @returns {object} Config with language, provider, voice
   */
  _getTtsConfig(text) {
    // Arabic detection
    if (/[\u0600-\u06FF]/.test(text)) {
      return {
        language: 'ar',
        provider: 'openai',
        voice: 'echo',
        useWebSpeech: false
      };
    }

    // Italian detection — requires at least 2 Italian function/grammar words.
    // Technical nouns like "Mandrini" or "Torretta" appear in English sentences too,
    // so a single borrowed term must not override an otherwise English message.
    const italianFunctionWords = (text.match(
      /\b(il|la|di|con|per|che|del|della|delle|dei|gli|le|uno|una|sono|è|ho|ha|come|cosa|questo|questa)\b/gi
    ) || []).length;
    if (italianFunctionWords >= 2) {
      return {
        language: 'it-IT',
        provider: 'openai',
        voice: 'onyx',
        useWebSpeech: false
      };
    }

    // Default English
    return {
      language: 'en-US',
      provider: 'aws-polly',
      voice: 'Matthew',
      useWebSpeech: false
    };
  }

  /**
   * Speaks text using a pre-resolved TTS config.
   * Config is determined once per conversation turn, not per sentence.
   * @param {string} text - Text to speak
   * @param {object} config - TTS config from _getTtsConfig()
   * @param {function():void} [onDone] - Callback when done
   */
  async speak(text, config, onDone) {
    if (!this.enabled || !text.trim()) {
      onDone?.();
      return;
    }

    this.stop();
    this.isSpeaking = true;

    const MAX_TTS_CHARS = 1000;
    const utterText = text.length > MAX_TTS_CHARS
      ? text.slice(0, MAX_TTS_CHARS) + "..."
      : text;

    // config is passed in — already resolved for this conversation turn

    try {
      await this._ensureSdkLoaded();

      // Try Puter SDK first
      const audio = await puter.ai.txt2speech(utterText, {
        language: config.language,
        provider: config.provider,
        engine: config.engine,
        voice: config.voice
      });

      this._currentAudio = audio;
      // English benefits from a slight speed-up; Arabic and Italian feel
      // rushed at 1.2× — keep them at natural speed.
      this._currentAudio.playbackRate = config.language === 'en-US' ? 1.0 : 1.12;
      this._currentAudio.onended = () => {
        this.isSpeaking = false;
        onDone?.();
      };
      this._currentAudio.onerror = (e) => {
        console.warn("[TTS] Puter playback error, falling back:", e);
        this._fallbackToWebSpeech(utterText, config.language, onDone);
      };

      await this._currentAudio.play();

    } catch (error) {
      // Puter failed with 400 - use Web Speech API fallback
      console.warn("[TTS] Puter SDK failed, using Web Speech fallback:", error.message);
      this._fallbackToWebSpeech(utterText, config.language, onDone);
    }
  }

  /**
   * Fallback to browser Web Speech API when Puter fails
   * @param {string} text - Text to speak
   * @param {string} lang - Language code
   * @param {function():void} [onDone] - Callback
   */
  _fallbackToWebSpeech(text, lang, onDone) {
    if (!('speechSynthesis' in window)) {
      console.error("[TTS] No TTS available");
      this.isSpeaking = false;
      onDone?.();
      return;
    }

    // Map AWS codes to Web Speech codes
    const langMap = {
      'arb': 'ar-SA',
      'it-IT': 'it-IT',
      'en-US': 'en-US'
    };

    const utterance = new SpeechSynthesisUtterance(text);
    utterance.lang = langMap[lang] || 'en-US';
    utterance.rate = 0.9;
    utterance.pitch = 1.0;

    utterance.onend = () => {
      this.isSpeaking = false;
      onDone?.();
    };

    utterance.onerror = (e) => {
      console.error("[TTS] Web Speech error:", e);
      this.isSpeaking = false;
      onDone?.();
    };

    window.speechSynthesis.speak(utterance);
  }

  stop() {
    if (this._currentAudio) {
      this._currentAudio.pause();
      this._currentAudio.currentTime = 0;
      this._currentAudio = null;
    }
    window.speechSynthesis.cancel();
    this.isSpeaking = false;
  }

  toggle() {
    this.enabled = !this.enabled;
    if (!this.enabled) this.stop();
    return this.enabled;
  }
}


