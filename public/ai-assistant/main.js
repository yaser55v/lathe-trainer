// ============================================================
//  NVIDIA NIM STREAMING CLIENT
//  Uses native browser fetch + ReadableStream to parse SSE chunks.
//  No external npm packages. No OpenAI SDK dependency.
// ============================================================

class NvidiaStreamClient {
  /**
   * @param {string} apiKey  — NVIDIA NIM API key
   */
  constructor(apiKey) {
    this.apiKey = apiKey;
    this.endpoint = `${NVIDIA_BASE_URL}/chat/completions`;
    this._controller = null; // AbortController for stream cancellation
  }

  /**
   * Sends a streaming chat completion request.
   *
   * @param {Array<{role:string, content:string}>} messages — Full message history
   * @param {Object} callbacks
   * @param {function(string):void}  callbacks.onToken          — Called for each visible content token
   * @param {function(string):void}  callbacks.onReasoningToken — Called for each reasoning/thinking token
   * @param {function():void}        callbacks.onDone           — Called when stream closes cleanly
   * @param {function(Error):void}   callbacks.onError          — Called on network or parse error
   * @returns {function():void}  abort() function to cancel the stream
   */
  stream(messages, { onToken, onReasoningToken, onDone, onError }) {
    this._controller = new AbortController();
    const { signal } = this._controller;

    (async () => {
      let response;

      try {
        response = await fetch(this.endpoint, {
          method: "POST",
          headers: {
            "Content-Type": "application/json",
            "Authorization": `Bearer ${this.apiKey}`,
          },
          signal,
          body: JSON.stringify({
            model: MODEL_ID,
            messages,
            stream: true,
            ...MODEL_PARAMS,
          }),
        });
      } catch (err) {
        if (err.name === "AbortError") return;
        onError(new Error(`Network error: ${err.message}`));
        return;
      }

      if (!response.ok) {
        let detail = "";
        try { detail = await response.text(); } catch { /* ignore */ }
        onError(new Error(`API error ${response.status}: ${detail}`));
        return;
      }

      const reader = response.body.getReader();
      const decoder = new TextDecoder("utf-8");
      let buffer = "";

      // Streaming SSE parse loop
      while (true) {
        let done, value;
        try {
          ({ done, value } = await reader.read());
        } catch (err) {
          if (err.name === "AbortError") break;
          onError(new Error(`Stream read error: ${err.message}`));
          break;
        }

        if (done) break;

        // Decode the incoming Uint8Array chunk and append to buffer.
        // The { stream: true } option ensures multi-byte chars across
        // chunk boundaries are handled correctly by the TextDecoder.
        buffer += decoder.decode(value, { stream: true });

        // SSE events are separated by double newlines; each line begins with "data: "
        const lines = buffer.split("\n");

        // Keep the last (potentially incomplete) line in the buffer
        buffer = lines.pop() ?? "";

        for (const line of lines) {
          const trimmed = line.trim();
          if (!trimmed || !trimmed.startsWith("data:")) continue;

          const payload = trimmed.slice(5).trim();

          // SSE stream termination sentinel
          if (payload === "[DONE]") {
            onDone();
            return;
          }

          let parsed;
          try {
            parsed = JSON.parse(payload);
          } catch (parseErr) {
            console.debug("[SSE] Skipped non-JSON payload:", payload);
            continue;
          }

          const delta = parsed?.choices?.[0]?.delta;
          if (!delta) continue;

          // --- Reasoning / thinking content (diagnostic only) ---
          const reasoning = delta.reasoning_content;
          if (reasoning) {
            onReasoningToken(reasoning);
          }

          // --- Visible content tokens ---
          const content = delta.content;
          if (content) {
            onToken(content);
          }
        }
      }

      // Flush any remaining buffered bytes from TextDecoder
      const remaining = decoder.decode(new Uint8Array(), { stream: false });
      if (remaining) {
        // Try to parse any final payload in the remainder
        const finalTrimmed = remaining.trim();
        if (finalTrimmed.startsWith("data:") && finalTrimmed.slice(5).trim() !== "[DONE]") {
          try {
            const finalParsed = JSON.parse(finalTrimmed.slice(5).trim());
            const finalContent = finalParsed?.choices?.[0]?.delta?.content;
            if (finalContent) onToken(finalContent);
          } catch { /* not parseable, ignore */ }
        }
      }

      onDone();
    })();

    // Return an abort handle so callers can cancel mid-stream
    return () => {
      if (this._controller) this._controller.abort();
    };
  }

  abort() {
    if (this._controller) this._controller.abort();
  }
}

// ============================================================
//  ACTION INTERCEPTOR
//  Strips [ACTION:*] tokens from visible text and fires UI events.
// ============================================================

class ActionInterceptor {
  /**
   * @param {function(string, string):void} onAction
   *   Called with (actionType, rawToken) when a token is intercepted.
   */
  constructor(onAction) {
    this.onAction = onAction;
  }

  /**
   * Strips all action tokens from the text and fires onAction callbacks.
   * @param {string} text
   * @returns {string} — cleaned text with tokens removed
   */
  process(text) {
    const found = [];
    const cleaned = text.replace(ACTION_TOKEN_PATTERN, (match, actionType) => {
      found.push({ match, actionType });
      return ""; // remove from visible output
    });

    for (const { match, actionType } of found) {
      console.info(`[ActionInterceptor] Intercepted: ${match}`);
      this.onAction(actionType, match);
    }

    return cleaned;
  }
}

